/**
 * creates an array with random numbers.
 *
 * @param n amount of numbers
 * @returns {number[]} `array` of random numbers
 */
export function randomNumbers(n: number): number[] {
    return Array(n)
        .fill(0)
        .map(() => Math.floor(Math.random() * n));
}

